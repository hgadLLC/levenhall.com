// Generates the DC-centered azimuthal equidistant map as an Astro component.
// Real coastlines (world-atlas 110m), graticule, range rings, and geodesic
// arcs from Washington to each allied capital. Run once:
//   node scripts/generate-azimuthal.mjs
import { geoAzimuthalEquidistant, geoPath, geoGraticule, geoCircle, geoDistance } from 'd3-geo';
import { feature } from 'topojson-client';
import { readFileSync, writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const world = JSON.parse(readFileSync(require.resolve('world-atlas/land-110m.json'), 'utf8'));
const land = feature(world, world.objects.land);

const W = 720, H = 720;
const DC = [-77.0369, 38.9072];

const capitals = [
    { name: 'WASHINGTON', coords: DC, home: true },
    { name: 'OTTAWA', coords: [-75.6972, 45.4215] },
    { name: 'LONDON', coords: [-0.1276, 51.5074] },
    { name: 'CANBERRA', coords: [149.1300, -35.2809] },
    { name: 'WELLINGTON', coords: [174.7762, -41.2865] },
];

const projection = geoAzimuthalEquidistant()
    .rotate([-DC[0], -DC[1]])
    .translate([W / 2, H / 2])
    .scale(114) // full world fits: max distance ≈ π, π·scale ≈ 358px ≤ 360
    .precision(0.1);

const path = geoPath(projection);
const fmt = (d) => d.replace(/(\.\d{2})\d+/g, '$1');

// Land
const landPath = fmt(path(land));

// Graticule every 20°
const graticulePath = fmt(path(geoGraticule().step([20, 20])()));

// Range rings: geodesic circles around DC at 30/60/90/120/150° angular radius
const rings = [30, 60, 90, 120, 150].map(r =>
    fmt(path(geoCircle().center(DC).radius(r)()))
);

// Geodesic arcs DC -> capitals (LineString follows great circle under d3 adaptive sampling)
const arcs = capitals.slice(1).map(c =>
    fmt(path({ type: 'LineString', coordinates: [DC, c.coords] }))
);

// Capital points
const pts = capitals.map(c => {
    const [x, y] = projection(c.coords);
    return { name: c.name, x: +x.toFixed(1), y: +y.toFixed(1), home: !!c.home };
});

// Label offsets tuned per capital to avoid collisions with linework
const labelOffset = {
    WASHINGTON: [10, 4],
    OTTAWA: [9, -5],
    LONDON: [9, -6],
    CANBERRA: [-72, 14],
    WELLINGTON: [12, 14],
};

const component = `---
// AUTO-GENERATED by scripts/generate-azimuthal.mjs — edit that script, not this file.
// Azimuthal equidistant projection centered on Washington DC. Real 110m coastlines.
interface Props { class?: string; }
const { class: className = '' } = Astro.props;
---
<svg viewBox="0 0 ${W} ${H}" class={\`azimuthal-map \${className}\`} role="img"
    aria-label="Azimuthal equidistant world map centered on Washington DC with geodesic arcs to Ottawa, London, Canberra and Wellington">
    <defs>
        <clipPath id="az-clip"><circle cx="${W / 2}" cy="${H / 2}" r="${Math.round(Math.PI * 114)}"/></clipPath>
    </defs>
    <!-- outer instrument ring -->
    <circle cx="${W / 2}" cy="${H / 2}" r="${Math.round(Math.PI * 114) + 1}" class="az-outer"/>
    <g clip-path="url(#az-clip)">
        <path class="az-graticule" d="${graticulePath}"/>
        <path class="az-land" d="${landPath}"/>
        ${rings.map((r, i) => `<path class="az-ring" data-ring="${(i + 1) * 30}" d="${r}"/>`).join('\n        ')}
        ${arcs.map((a, i) => `<path class="az-arc" style="--arc-i:${i}" d="${a}"/>`).join('\n        ')}
    </g>
    <!-- degree ticks on the outer ring -->
    <g class="az-ticks">
        ${Array.from({ length: 72 }, (_, i) => {
            const a = (i * 5 * Math.PI) / 180;
            const R = Math.PI * 114;
            const len = i % 6 === 0 ? 10 : 5;
            const x1 = W / 2 + Math.cos(a) * R, y1 = H / 2 + Math.sin(a) * R;
            const x2 = W / 2 + Math.cos(a) * (R + len), y2 = H / 2 + Math.sin(a) * (R + len);
            return `<line x1="${x1.toFixed(1)}" y1="${y1.toFixed(1)}" x2="${x2.toFixed(1)}" y2="${y2.toFixed(1)}"/>`;
        }).join('\n        ')}
    </g>
    ${pts.map(p => {
        const [dx, dy] = labelOffset[p.name] ?? [9, 4];
        return `<g class={\`az-cap \${${p.home} ? 'az-home' : ''}\`}>
        <circle cx="${p.x}" cy="${p.y}" r="${p.home ? 4 : 2.8}" class="az-pt${p.home ? ' az-pt-home' : ''}"/>${p.home ? `\n        <circle cx="${p.x}" cy="${p.y}" r="4" class="az-ping"/>` : ''}
        <text x="${p.x + dx}" y="${p.y + dy}" class="az-label">${p.name}</text>
    </g>`;
    }).join('\n    ')}
</svg>

<style>
    .azimuthal-map { width: 100%; height: auto; display: block; }

    /* Engraved linework — colors resolve per surface via currentColor-ish vars */
    .az-outer { fill: none; stroke: var(--az-strong, #2a3548); stroke-width: 1; }
    .az-graticule { fill: none; stroke: var(--az-faint, rgba(42,53,72,.5)); stroke-width: 0.5; }
    .az-land { fill: var(--az-land, rgba(201,169,97,0.05)); stroke: var(--az-coast, #55617a); stroke-width: 0.7; }
    .az-ring { fill: none; stroke: var(--az-strong, #2a3548); stroke-width: 0.6; stroke-dasharray: 1 5; }
    .az-arc {
        fill: none; stroke: var(--gold, #c9a961); stroke-width: 1;
        stroke-dasharray: 4 4;
        animation: az-flow 30s linear infinite;
        animation-delay: calc(var(--arc-i, 0) * -7.5s);
    }
    .az-ticks line { stroke: var(--az-strong, #2a3548); stroke-width: 0.8; }
    .az-pt { fill: var(--az-pt, #8a94a8); }
    .az-pt-home { fill: var(--burgundy, #8b1538); }
    .az-ping {
        fill: none; stroke: var(--gold, #c9a961); stroke-width: 1;
        transform-origin: center; transform-box: fill-box;
        animation: az-ping 3.2s cubic-bezier(0.16, 1, 0.3, 1) infinite;
    }
    .az-label {
        font-family: var(--mono, monospace);
        font-size: 11px;
        letter-spacing: 0.1em;
        fill: var(--az-text, #8a94a8);
    }
    .az-home .az-label { fill: var(--az-text-home, #fafaf8); }

    @keyframes az-flow { to { stroke-dashoffset: -240; } }
    @keyframes az-ping {
        0% { transform: scale(1); opacity: 0.9; }
        80%, 100% { transform: scale(4.5); opacity: 0; }
    }

    @media (prefers-reduced-motion: reduce) {
        .az-arc, .az-ping { animation: none; }
        .az-ping { opacity: 0; }
    }
</style>
`;

writeFileSync(new URL('../src/components/AzimuthalMap.astro', import.meta.url), component);

// sanity: report geodesic distances (angular, degrees)
for (const c of capitals.slice(1)) {
    console.log(c.name, (geoDistance(DC, c.coords) * 180 / Math.PI).toFixed(1) + '°');
}
console.log('Wrote src/components/AzimuthalMap.astro');
